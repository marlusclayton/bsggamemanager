package com.bsg.state;

public class DuplicateCharacterException extends Exception {

	public DuplicateCharacterException() {
		super();
	}

	public DuplicateCharacterException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public DuplicateCharacterException(String msg) {
		super(msg);
	}

	public DuplicateCharacterException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1022452489014212906L;

}
